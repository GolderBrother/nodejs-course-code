#!/bin/bash

# 遍历当前目录下的所有文件夹
for dir in */; do
    # 检查是否是目录
    if [ -d "$dir" ]; then
        # 检查 node_modules 是否存在
        if [ -d "$dir/node_modules" ]; then
            echo "Deleting $dir/node_modules"
            rm -rf "$dir/node_modules"  # 删除 node_modules 文件夹
        fi
    fi
done

echo "All node_modules folders have been deleted."